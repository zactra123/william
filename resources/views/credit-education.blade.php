            <h5 class="servicespagetitle">Purpose of credit scores</h5>
            <p class="fs-18">Lenders use credit scores as a great decision-making tool as it helps them in analyzing whether you will be able to repay your loan at the specified time or not. These scores are also known as risk scores as they help the lenders in determining the risk of giving you a loan and whether you will repay the debt on time. Therefore, having a good credit score is important as it will help you get your loan approved by the lender. These credit scores are like your report cards. They help in determining whether you qualify for the credit or not.
                </br></br>
                However, it is crucial to note that your credit score alone does not help in making the lender decide if you qualify for the loan or not. There is other information mentioned on your credit report such as the following which helps in getting your loan approved:</p>

            <ul class="fs-18">
                <li>The total amount of debt</li>
                <li>Types of credit</li>
                <li>The time that is taken to pay off debts</li>
                <li>Help determine your total monthly expenses</li>
                <li>The ratio of monthly expenses against your income.</li>
            </ul>
            <h5 class="servicespagetitle">Factors affecting credit scores</h5>
            <p class="fs-18">There are certain factors in your credit report that may affect your overall credit score. These factors are mentioned below:</p>
            <ol class="fs-18">
                <li>Your payment history that includes loan and credit cards information</li>
                <li>Your credit card utilization rate</li>
                <li>The total amount of debt due</li>
                <li>If there is any public record against you, such as a bankruptcy.</li>
                <li>If any inquiry took place against your credit report</li>
                <li>Amount of credit accounts you have.</li>
            </ol>
